/**
 * ONE_DAY_IN_MILLISECONDS is a constant that holds the number of milliseconds in one day.
 */

export const ONE_DAY_IN_MILLISECONDS = 86400000;

/**
 * ONE_WEEK_IN_MILLISECONDS is a constant that holds the number of milliseconds in one week.
 */

export const ONE_WEEK_IN_MILLISECONDS = ONE_DAY_IN_MILLISECONDS * 7;

/**
 * MILLISECONDS_MULTIPLIER is a constant that holds the value used to convert seconds to milliseconds.
 */

export const MILLISECONDS_MULTIPLIER = 1000;
