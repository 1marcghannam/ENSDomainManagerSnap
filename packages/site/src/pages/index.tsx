const Index = () => {
  const [state, dispatch] = useContext(MetaMaskContext);
  const [ensDomain, setEnsDomain] = useState('');

  const handleConnectClick = async () => {
    try {
      await connectSnap();
      const installedSnap = await getSnap();

      dispatch({
        type: MetamaskActions.SetInstalled,
        payload: installedSnap,
      });
    } catch (e) {
      console.error(e);
      dispatch({ type: MetamaskActions.SetError, payload: e });
    }
  };

  const handleAddOrRemoveClick = async () => {
    try {
      await sendHello();
    } catch (e) {
      console.error(e);
      dispatch({ type: MetamaskActions.SetError, payload: e });
    }
  };

  return (
    <Container>
      <Heading>
        <Span>ENS Domain Manager</Span>
      </Heading>
      <Subtitle>
        Create notification alers for your ENS domains. <br />
      </Subtitle>
      <CardContainer>
        {state.error && (
          <ErrorMessage>
            <b>An error happened:</b> {state.error.message}
          </ErrorMessage>
        )}
        {!state.isFlask && (
          <Card
            content={{
              title: 'Install',
              description:
                'Snaps is pre-release software only available in MetaMask Flask, a canary distribution for developers with access to upcoming features.',
              button: <InstallFlaskButton />,
            }}
            fullWidth
          />
        )}
        {!state.installedSnap && (
          <Card
            content={{
              title: 'Connect',
              description:
                'Get started by connecting to and installing the example snap.',
              button: (
                <ConnectButton
                  onClick={handleConnectClick}
                  disabled={!state.isFlask}
                />
              ),
            }}
            disabled={!state.isFlask}
          />
        )}
        {shouldDisplayReconnectButton(state.installedSnap) && (
          <Card
            content={{
              title: 'Reconnect',
              description:
                'While connected to a local running snap this button will always be displayed in order to update the snap if a change is made.',
              button: (
                <ReconnectButton
                  onClick={handleConnectClick}
                  disabled={!state.installedSnap}
                />
              ),
            }}
            disabled={!state.installedSnap}
            fullWidth={true}
          />
        )}
        <Card
          content={{
            title: 'Add ENS Domain Name',
            description:
              'Create a MetaMask notification to remind about the expiration of your ENS Domain Names.',
            button: (
              <AddOrRemoveButton
                onClick={handleAddOrRemoveClick}
                disabled={!state.installedSnap}
              />
            ),
            input: (
              <input
                type="text"
                placeholder="Enter your ENS Domain Name"
                style={{
                  marginTop: '1.4rem',
                  marginBottom: '4.4rem',
                  paddingTop: '1rem',
                  paddingBottom: '1rem',
                  paddingLeft: '1.6rem',
                }}
              />
            ),
          }}
          disabled={!state.installedSnap}
          fullWidth={true}
        />
      </CardContainer>
    </Container>
  );
};

export default Index;
